package com.simminjeong.algorithm.pro;


public class Constructionsite {

    private int noisyr;
    private Position position;

    public Constructionsite(int noisyr, Position position) {
        this.noisyr = noisyr;
        this.position = position;
    }

    public int getNoisyr() {
        return noisyr;
    }

    public void setNoisyr(int noisyr) {
        this.noisyr = noisyr;
    }

    public Position getPosition() {
        return position;
    }

    public void setPosition(Position position) {
        this.position = position;
    }



}
